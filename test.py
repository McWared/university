# Цифра, яку ми шукаємо
b = '1'

# Лічильник кількості чисел, в яких є цифра b
count = 0

# Перевіряємо кожну п'ятизначну комбінацію
for number in range(10000, 100000):
    if b in str(number):
        count += 1

# Виводимо результат
print(f"Кількість п'ятизначних чисел, в яких є цифра {b}: {count}")